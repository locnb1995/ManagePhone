package com.baoloc.hus.manage.commons;

public final class AppConstants
{

	public final static String get_find_data_error_message(String entity)
	{
		return "Can not find " + entity;
	}

	public final static String get_update_data_success_message(String entity)
	{
		return "Update " + entity + " success";
	}

	public final static String get_delete_data_success_message(String entity)
	{
		return "Delete " + entity + " success";
	}

	public final static String get_create_data_success_message(String entity)
	{
		return "Create " + entity + " success";
	}
}
